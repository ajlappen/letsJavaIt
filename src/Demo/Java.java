package Demo;

import java.util.Scanner;

public class Java {

    public static void main(String[] args) {
        //Arithmetic operators
        // + - * / %
        // = - assignment operators
        // Logical operator
        // <, >, ==, !=


        int a = 5;
        int b = 6;


        boolean flag = a > b;
        if (flag){
            System.out.println("a>b: " + "a" + " = " + a);
        }else {
            System.out.println("b>a: " + "b" + " = " + b);
        }
    }
}
